char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	aux;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	i = i - 1;
	while (j < i)
	{
		aux = str[j];
		str[j] = str[i];
		str[i] = aux;
		i--;
		j++;
	}
	return (str);
}
