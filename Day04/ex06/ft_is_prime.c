int		ft_is_prime(int nb)
{
	int		i;

	if (nb == 0 || nb == 1)
		return (0);
	else if (nb == 2)
		return (1);
	else
	{
		i = 2;
		if (nb < 0)
			nb = nb * (-1);
		while (i <= nb / 2)
		{
			if (nb % i == 0)
				return (0);
			i++;
		}
	}
	return (1);
}
