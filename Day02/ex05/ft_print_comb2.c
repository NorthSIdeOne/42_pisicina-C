int		ft_putchar(char c);

void	ft_print_comb2(void)
{
	int		nr_1_2;
	int		nr_2_2;

	nr_1_2 = 0;
	nr_2_2 = 0;
	while (nr_1_2 != 99)
	{
		while (nr_2_2 != 99)
		{
			nr_2_2++;
			if (nr_2_2 > 1)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
			ft_putchar(((nr_1_2 / 10) + '0'));
			ft_putchar(((nr_1_2 % 10) + '0'));
			ft_putchar(' ');
			ft_putchar(((nr_2_2 / 10) + '0'));
			ft_putchar(((nr_2_2 % 10) + '0'));
		}
		nr_1_2++;
		nr_2_2 = nr_1_2;
	}
}
